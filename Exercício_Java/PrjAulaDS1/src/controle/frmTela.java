    private void BtnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGravarActionPerformed
        // TODO add your handling code here:
        String quant = Campoquant.getText();
        String desc = Campodesc.getText();
        String val = Campoval.getText();
        String forn = Campoforn.getText();
        
        try {
            String insert_sql = "insert into produtos(quantidade, descricao, Valor, id_fornecedor) values ('" + quant + "','" + desc + "','" + val + "','" + forn + "')";
            con_cliente.statement.executeUpdate(insert_sql);
            JOptionPane.showMessageDialog(null, "Gravação relaizada com sucesso!!", "Mensagem do Programa", JOptionPane.INFORMATION_MESSAGE);
            con_cliente.executaSQL("select * from produtos order by id_produto");
            con_cliente.resultset.first();
            mostrar_Dados();
            preencherTabela();

        } catch (SQLException errosql) {
            JOptionPane.showMessageDialog(null, "/n Erro na gravação: /n" + errosql,
                "mensagem so programa", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_BtnGravarActionPerformed

