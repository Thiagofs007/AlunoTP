    public void preencherTabela() {
        produtos.getColumnModel().getColumn(0).setPreferredWidth(4);
        produtos.getColumnModel().getColumn(1).setPreferredWidth(150);
        produtos.getColumnModel().getColumn(2).setPreferredWidth(11);
        produtos.getColumnModel().getColumn(3).setPreferredWidth(14);
        produtos.getColumnModel().getColumn(4).setPreferredWidth(100);

        DefaultTableModel modelo = (DefaultTableModel) produtos.getModel();
        modelo.setNumRows(0);

        try {
            con_cliente.resultset.beforeFirst();
            while (con_cliente.resultset.next()) {
                modelo.addRow(new Object[]{
                    con_cliente.resultset.getString("id_produto"), con_cliente.resultset.getString("quantidade"),
                    con_cliente.resultset.getString("descricao"), con_cliente.resultset.getString("Valor"),
                    con_cliente.resultset.getString("id_fornecedor")
                }
                );

            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "/n Erro ao listar dados da tabela!!: /n" + erro,
                    "mensagem so programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void posicionarRegistros() {
        try {
            con_cliente.resultset.first();
            mostrar_Dados();
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "/n Não foi possivel posicionar no primeiro registro: " + erro,
                    "mensagem so programa", JOptionPane.INFORMATION_MESSAGE);

        }
    }

    public void mostrar_Dados() {
        try {
            Campopro.setText(con_cliente.resultset.getString("id_produto"));
            Campoquant.setText(con_cliente.resultset.getString("quantidade"));
            Campodesc.setText(con_cliente.resultset.getString("descricao"));
            Campoval.setText(con_cliente.resultset.getString("Valor"));
            Campoforn.setText(con_cliente.resultset.getString("id_fornecedor"));
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "/n Não localizou dados: " + erro,
                    "mensagem so programa", JOptionPane.INFORMATION_MESSAGE);

        }
    }

